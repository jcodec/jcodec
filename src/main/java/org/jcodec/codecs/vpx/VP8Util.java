    /**
     * delta() is the process to read 5 bits from the bitstream to determine a
     * signed delta value:
     * 
     * +-------+--------------------------------------------------+ | Index |
     * Description | +-------+--------------------------------------------------+ |
     * L(4) | Magnitude of delta | | L(1) | Sign of delta, 0 for positive and 1 for
     * negative | +-------+--------------------------------------------------+
     * 
     * @param bc
     * @return
     * @throws IOException
     */
    public static int delta(VPXBooleanDecoder bc) {
        int magnitude = bc.decodeInt(4);

        if (bc.readBitEq() > 0)
            magnitude = -magnitude;
        return magnitude;

    }

