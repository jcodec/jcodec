    public static short[] pickDefaultPrediction(BPredictionMode intra_bmode) {
        if (intra_bmode == BPredictionMode.B_TM_PRED || intra_bmode == BPredictionMode.B_DC_PRED
                || intra_bmode == BPredictionMode.B_VE_PRED || intra_bmode == BPredictionMode.B_HE_PRED
                || intra_bmode == BPredictionMode.B_VR_PRED || intra_bmode == BPredictionMode.B_RD_PRED
                || intra_bmode == BPredictionMode.B_HD_PRED)
            return PRED_BLOCK_129;

        return PRED_BLOCK_127;
    }

